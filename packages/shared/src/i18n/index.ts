export type TranslationKey = keyof typeof translations.en;
export type Locale = 'ar' | 'en';
export type Namespace = keyof typeof translations.en;

export function t(
  key: string,
  locale: Locale = 'en',
  ns: Namespace = 'common',
): string {
  const keys = key.split('.');
  let value: Record<string, unknown> | string | undefined =
    locale === 'ar' ? translations.ar : translations.en;

  for (const k of [ns, ...keys]) {
    if (value && typeof value === 'object' && k in value) {
      value = (value as Record<string, unknown>)[k] as string | Record<string, unknown>;
    } else {
      return key;
    }
  }
  return typeof value === 'string' ? value : key;
}

export function getDir(locale: Locale): 'rtl' | 'ltr' {
  return locale === 'ar' ? 'rtl' : 'ltr';
}
